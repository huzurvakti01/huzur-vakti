  static String formatClock({
    required int hour,
    required int minute,
  }) {
    return '${hour.toString().padLeft(2, '0')}:${minute.toString().padLeft(2, '0')}';
  }

  static String formatDurationClock({
    required String hours,
    required String minutes,
    required String seconds,
  }) {
    return '$hours:$minutes:$seconds';
  }

  static String formatIndex(int value) => '$value';
  static String formatKm(int value) => '$value km';

  static String format(String template, Map<String, Object> values) {
    var result = template;
    for (final entry in values.entries) {
      result = result.replaceAll('{${entry.key}}', entry.value.toString());
    }
    return result;
  }
